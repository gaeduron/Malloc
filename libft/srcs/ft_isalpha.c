static int		is_alpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ? 1 : 0);
}

int				ft_isalpha(int c)
{
	if (!is_alpha(c))
		return (0);
	return (1);
}
